    fun getAllSame(key: String): Array<Translate> {
        val ret = ArrayList<Translate>()
        val k = key.lowercase(Locale.getDefault())
        for (t in list) if (t.key.lowercase(Locale.getDefault()).contains(k)) ret.add(t)
        return ret.toTypedArray()
    }

    fun getAllSame(stringKey: String, minimumSize: Int = 0): Array<Translate> {
        var index = 1
        val list = ArrayList<Translate>()
        while (true) {
            val x = getAllSame(if (stringKey.contains("%s")) String.format(stringKey, index) else stringKey + index)
            index++
            if (x.isNotEmpty()) list.add(x[0])
            else if (index <= minimumSize+1) list.add(Translate(""))
            else break
        }
        return list.toTypedArray()
    }

    fun put(t: Translate){
        list.add(t)
        map[t.key] = t
